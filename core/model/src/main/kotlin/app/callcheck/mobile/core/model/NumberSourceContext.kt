package app.callcheck.mobile.core.model

/**
 * 번호가 유입된 경로(출처).
 *
 * ┌──────────────────────────────────────────────────────┐
 * │ 기기 컨텍스트 동기화 원칙                              │
 * ├──────────────────────────────────────────────────────┤
 * │ 번호 형식은 기기가 제공하는 원본(raw)을 1차 기준으로   │
 * │ 유지한다. 앱이 자체 규칙으로 형식을 정의하지 않는다.   │
 * │ sourceContext는 raw 번호가 어디서 왔는지를 명시하여    │
 * │ 파싱/검색 전략 결정의 근거로 사용된다.                 │
 * └──────────────────────────────────────────────────────┘
 */
enum class NumberSourceContext {

    /** CallScreeningService를 통해 수신된 실시간 착신 번호 */
    INCOMING_CALL,

    /** 기기 통화 기록(CallLog)에서 읽어온 번호 */
    CALL_LOG,

    /** SMS/MMS 수신 메시지에서 읽어온 번호 */
    SMS,

    /** 기기 연락처(Contacts Provider)에서 읽어온 번호 */
    CONTACT,
}
